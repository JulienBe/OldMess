package elements.particular.particles.shards;

public class ShardModel {
	
	public final float initX, initY, initColor, initDirX, initDirY;

	public ShardModel(float initX, float initY, float initColor, float initDirX, float initDirY) {
		super();
		this.initX = initX;
		this.initY = initY;
		this.initColor = initColor;
		this.initDirX = initDirX;
		this.initDirY = initDirY;
	}
	
}
